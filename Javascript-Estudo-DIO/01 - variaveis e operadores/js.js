const nome = "Yhago";
var idade = 18; // Número
let estado = "São Paulo";
let isDev = true; // Boolean

console.log(`Meu nome é ${nome}, tenho ${idade} anos, atualmente resido no estado de ${estado}, e sou dev! Essa resposta é ${isDev}`);

// Operadores Aritméticos
let a = 10;
let b = 5;

let soma = a + b; // Adição
let subtracao = a - b; // Subtração
let multiplicacao = a * b; // Multiplicação
let divisao = a / b; // Divisão
let modulo = a % b; // Módulo

console.log(`Soma: ${soma}`);
console.log(`Subtração: ${subtracao}`);
console.log(`Multiplicação: ${multiplicacao}`);
console.log(`Divisão: ${divisao}`);
console.log(`Módulo: ${modulo}`);

// Operadores de Comparação
let x = 10;
let y = 5;

console.log(`x > y: ${x > y}`); // Maior que
console.log(`x < y: ${x < y}`); // Menor que
console.log(`x >= y: ${x >= y}`); // Maior ou igual a
console.log(`x <= y: ${x <= y}`); // Menor ou igual a
console.log(`x == y: ${x == y}`); // Igual a (valor)
console.log(`x === y: ${x === y}`); // Igual a (valor e tipo)
console.log(`x != y: ${x != y}`); // Diferente de (valor)
console.log(`x !== y: ${x !== y}`); // Diferente de (valor e tipo)

// Operadores Lógicos
let verdadeiro = true;
let falso = false;

console.log(`verdadeiro && falso: ${verdadeiro && falso}`); // E lógico (AND)
console.log(`verdadeiro || falso: ${verdadeiro || falso}`); // OU lógico (OR)
console.log(`!verdadeiro: ${!verdadeiro}`); // NÃO lógico (NOT)
console.log(`!falso: ${!falso}`); // NÃO lógico (NOT)
